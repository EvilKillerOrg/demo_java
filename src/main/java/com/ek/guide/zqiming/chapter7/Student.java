package com.ek.guide.zqiming.chapter7;

public class Student {
	
	private String name;
	private int no;
	
	public Student(String name , int no){
		this.name=name;
		this.no=no;
	}

	
	
	public String toString() {
		 
		return "姓名: "+this.name+ "\t学号: "+this.no+"\n";
	}
	
	 

	
	
	
	

}